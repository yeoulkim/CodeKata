#include <stdio.h>
#include <stdbool.h>
#include <stdlib.h>

int solution(int num1, int num2)
{
    if(num1 == num2)
    {
        return 1;
    }
    else    // if나 else if에서 모든 조건 검사한 후 남은 경우 처리 용도 -> 조건 없음
    {
        return -1;
    }
}

